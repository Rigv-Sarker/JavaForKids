package java4Kids;

public class IfElseWithOr {

	public static void main(String[] args) {
		int unitCompletion = 1;
		int bubblesCompleted = 1;
		
		if (unitCompletion > 5 || bubblesCompleted >0) {
			System.out.println("You will get a treat! Yay :D !");
		}else {
			System.out.println("No treat, sorry! :(" );
		}
	}

}
